package jp.ossc.nimbus.service.http.proxy;

import jp.ossc.nimbus.core.*;

/**
 * {@link ProxyServerService}のMBeanインタフェース。<p>
 * 
 * @author M.Takata
 * @see ProxyServerService
 */
public interface ProxyServerServiceMBean extends ServiceBaseMBean{
    
    /**
     * デフォルトの待ち受けポート番号。<p>
     */
    public static final int DEFAULT_PORT = 8080;
    
    /**
     * {@link Process}サービスのサービス名を設定する。<p>
     *
     * @param name Processサービスのサービス名
     */
    public void setProcessServiceName(ServiceName name);
    
    /**
     * {@link Process}サービスのサービス名を取得する。<p>
     *
     * @return Processサービスのサービス名
     */
    public ServiceName getProcessServiceName();
    
    /**
     * {@link javax.net.ServerSocketFactory}サービスのサービス名を設定する。<p>
     *
     * @param name ServerSocketFactoryサービスのサービス名
     */
    public void setServerSocketFactoryServiceName(ServiceName name);
    
    /**
     * {@link javax.net.ServerSocketFactory}サービスのサービス名を取得する。<p>
     *
     * @return ServerSocketFactoryサービスのサービス名
     */
    public ServiceName getServerSocketFactoryServiceName();
    
    /**
     * サーバソケットのバインドアドレスを設定する。<p>
     *
     * @param address バインドアドレス
     */
    public void setBindAddress(String address);
    
    /**
     * サーバソケットのバインドアドレスを取得する。<p>
     *
     * @return バインドアドレス
     */
    public String getBindAddress();
    
    /**
     * サーバソケットのバックログを設定する。<p>
     * デフォルトは、0。<br>
     *
     * @param backlog バックログ
     */
    public void setBacklog(int backlog);
    
    /**
     * サーバソケットのバックログを取得する。<p>
     *
     * @return バックログ
     */
    public int getBacklog();
    
    /**
     * このプロキシの待ち受けポート番号を設定する。<p>
     * デフォルトは、{@link #DEFAULT_PORT}。<br>
     *
     * @param port ポート番号
     */
    public void setPort(int port);
    
    /**
     * このプロキシの待ち受けポート番号を取得する。<p>
     *
     * @return ポート番号
     */
    public int getPort();
    
    /**
     * ソケットの読み込みタイムアウト[ms]を設定する。<p>
     * デフォルトは、タイムアウトしない。<br>
     *
     * @param millis 読み込みタイムアウト[ms]
     */
    public void setSoTimeout(int millis);
    
    /**
     * ソケットの読み込みタイムアウト[ms]を取得する。<p>
     *
     * @return 読み込みタイムアウト[ms]
     */
    public int getSoTimeout();
    
    /**
     * リクエスト処理スレッド数を設定する。<p>
     * デフォルトは、1。<br>
     *
     * @param count スレッド数
     */
    public void setMaxProcessCount(int count);
    
    /**
     * リクエスト処理スレッド数を取得する。<p>
     *
     * @return スレッド数
     */
    public int getMaxProcessCount();
}
