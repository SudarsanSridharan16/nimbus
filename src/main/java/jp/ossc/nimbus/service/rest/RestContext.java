package jp.ossc.nimbus.service.rest;

/**
 * RESTコンテキスト。<p>
 *
 * @author M.Takata
 */
public class RestContext{
    
    /**
     * RESTリクエスト。<p>
     */
    protected RestRequest request;
    
    /**
     * RESTレスポンス。<p>
     */
    protected RestResponse response;
    
    /**
     * 空のインスタンスを生成する。<p>
     */
    public RestContext(){
    }
    
    /**
     * RESTリクエストとRESTレスポンスを持ったインスタンスを生成する。<p>
     *
     * @param request RESTリクエスト
     * @param response RESTレスポンス
     */
    public RestContext(RestRequest request, RestResponse response){
        this.request = request;
        this.response = response;
    }
    
    /**
     * RESTリクエストを取得する。<p>
     * 
     * @return RESTリクエスト
     */
    public RestRequest getRequest(){
        return request;
    }
    
    /**
     * RESTリクエストを設定する。<p>
     * 
     * @param request RESTリクエスト
     */
    public void setRequest(RestRequest request){
        this.request = request;
    }
    
    /**
     * RESTレスポンスを取得する。<p>
     * 
     * @return RESTレスポンス
     */
    public RestResponse getResponse(){
        return response;
    }
    
    /**
     * RESTレスポンスを設定する。<p>
     * 
     * @param response RESTレスポンス
     */
    public void setResponse(RestResponse response){
        this.response = response;
    }
}