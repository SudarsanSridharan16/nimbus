package jp.ossc.nimbus.service.scheduler2;

/**
 * {@link CommandScheduleExecutorService}のMBeanインタフェース。<p>
 * 
 * @author M.Takata
 */
public interface CommandScheduleExecutorServiceMBean extends AbstractScheduleExecutorServiceMBean{
    
    /**
     * デフォルトのスケジュール実行種別。<p>
     */
    public static final String DEFAULT_EXECUTOR_TYPE = "COMMAND";
    
    /**
     * 作業ディレクトリを設定する。<p>
     * 指定しない場合は、JavaVMの作業ディレクトリ。<br>
     *
     * @param path 作業ディレクトリのパス
     */
    public void setWorkDirectory(String path);
    
    /**
     * 作業ディレクトリを取得する。<p>
     *
     * @return 作業ディレクトリのパス
     */
    public String getWorkDirectory();
    
    /**
     * 環境変数を設定する。<p>
     *
     * @param env 環境変数の配列
     */
    public void setEnvironmentVariables(String[] env);
    
    /**
     * 環境変数を取得する。<p>
     *
     * @return 環境変数の配列
     */
    public String[] getEnvironmentVariables();
    
    /**
     * プロセスの終了待ちをする場合に、プロセスが終了したかをチェックする間隔[ms]を設定する。<p>
     * デフォルトは、1秒。<br>
     *
     * @param interval チェック間隔[ms]
     */
    public void setCheckInterval(long interval);
    
    /**
     * プロセスの終了待ちをする場合に、プロセスが終了したかをチェックする間隔[ms]を取得する。<p>
     *
     * @return チェック間隔[ms]
     */
    public long getCheckInterval();
}
