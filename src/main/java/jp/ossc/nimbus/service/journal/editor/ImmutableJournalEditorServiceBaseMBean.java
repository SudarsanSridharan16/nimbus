package jp.ossc.nimbus.service.journal.editor;

import jp.ossc.nimbus.core.ServiceBaseMBean;
import jp.ossc.nimbus.service.journal.ImmutableJournalEditor;

/**
 * {@link ImmutableJournalEditorServiceBase}のMBeanインタフェース。<p>
 * 
 * @author M.Takata
 * @see ImmutableJournalEditorServiceBase
 */
public interface ImmutableJournalEditorServiceBaseMBean
 extends ServiceBaseMBean, ImmutableJournalEditor{
    
    /**
     * キーを出力するかどうかを設定する。<p>
     *
     * @param isOutput trueの場合、キー文字列を出力する
     */
    public void setOutputKey(boolean isOutput);
    
    /**
     * キーを出力するかどうかを判定する。<p>
     *
     * @return trueの場合、キー文字列を出力する
     */
    public boolean isOutputKey();
    
    /**
     * インデントを出力するかしないかを設定する。<p>
     * デフォルトはtrueで、出力する。<br>
     *
     * @param isOutput インデントを出力する場合true
     */
    public void setOutputIndent(boolean isOutput);
    
    /**
     * インデントを出力するかしないかを判定する。<p>
     *
     * @return trueの場合、インデントを出力する
     */
    public boolean isOutputIndent();
    
    /**
     * 改行文字列を設定する。<p>
     *
     * @param separator 改行文字列
     * @see #getLineSeparator()
     */
    public void setLineSeparator(String separator);
    
    /**
     * 改行文字列を取得する。<p>
     *
     * @return 改行文字列
     * @see #setLineSeparator(String)
     */
    public String getLineSeparator();
    
    /**
     * インデント文字列を設定する。<p>
     *
     * @param indent インデント文字列
     * @see #getIndent()
     */
    public void setIndent(String indent);
    
    /**
     * インデント文字列を取得する。<p>
     *
     * @return インデント文字列
     * @see #setIndent(String)
     */
    public String getIndent();
}
